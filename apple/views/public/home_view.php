    function register() {
        let data = new FormData();
        let hasRef = <?= $this->session->has_userdata('referal') ? $this->session->has_userdata('referal') : 'false' ?>;
        if (hasRef) {
            data.append('who_invite', "<?= $this->session->userdata('referal') ?>");
        }
        data.append('company_name', $('#company_name').val());
        data.append('company_phone', $('#company_phone').val());
        var fields = $('.hidden_field');
        for (var i = 0; i < fields.length; i++) {
            data.append($(fields[i]).attr('id'), $(fields[i]).val());
        }
        return $.ajax({
                url: '/public_ajax/registr_and_login/regstr',
                type: 'POST',
                data: data,
                processData: false,
                contentType: false,
                cache: false,
                async: false,
                success:
                    function(data) {
                        //валидируем данные
                        let result = validate_data_server_response(data);
                        insert_csrf_hash(data);
                        if (result) {
                            swal('Отлично!', data.text_message, 'success');
                            setTimeout(function() {
                                redirect('registration_form', 'success_registration', 'company/profile',
                                    data);
                            }, 1000);

                        } else {
                            swal('Ошибка!', '', 'error');
                        }
                    },
            },
        );
    }
